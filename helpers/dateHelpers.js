module.exports = {
  VALID_DATE: /[0-9]{4}-[0-9]{2}-[0-9]{2}/,

  format: function (date) {
    return [
      date.getFullYear(),
      (date.getMonth() + 1).toString().padStart(2, "0"),
      date.getDate().toString().padStart(2, "0"),
    ].join("-");
  },
};
